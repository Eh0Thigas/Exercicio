public class Exercicio4 {
    public static void main(String[] args) {
        
        // EXERCICIO EM SALA DE AULA 4
        //IMPRIMA TODOS OS FATORIAIS DE 1 A 10

        int fatorial = 1;

        for(int i = 1; i <= 10; i++){
            fatorial = fatorial * i;
            System.out.println(i+"! = "+fatorial);
        }
    }
}
